<h1>Over ons  <img src="img/about-us.png" alt="about-us" width="42" height="42" align="middle"> </h1>


      <p>Intré is een project van zeven informatiekunde studenten van de Universiteit Utrecht die de opdracht kregen om informatiesysteem te ontwerpen en implementeren dat een sociaal probleem oplost. </p>

        <p>Het resultaat is Intré. Een informatiesysteem dat als doel heeft een efficientere informatieuitwisseling tussen alle partijen betrokken bij de re-integratie van ex-delinquenten. Door een geïntegreerd web-based platform te creeren waar alle partijen toegang tot hebben, bieden wij een makkelijke manier van communiceren.</p>

        <p>Een goedwerkend systeem kan bijdragen aan de re-integratie nu bepaalde handelingen die de ex-delinquent eerst proactief moet oppakken automatisch kan.</p>

